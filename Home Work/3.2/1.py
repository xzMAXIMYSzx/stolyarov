# При помощи цикла while необходимо создать строку длиной не
# менее 10 символов * (звездочка), выводя эту строку на экран.
# 1я строка кода (скопируйте ее себе в редактор):
# base_str = “”
# 1. Добавляем переменную
base_str = ""
# 2. Вывод звездочек:
while len(base_str) != 10:
    base_str += "*"
    print(base_str)


