# 1. Записываем пару чисел, тип данных float
a = float(input('Введите первое число: '))
b = float(input('Введите второе число: '))
# 2. Проверяем какое больше
if a > b:
    print('Первое слово съела корова ', a)
elif b < a:
    print('Б тоже витамин ', b)
else:
    print('Они равны ')


